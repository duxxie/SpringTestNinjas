package com.example.ainda;

import jakarta.persistence.Entity;

@Entity
public class NinjaModel {

    long id;
    String nome;
    String email;
    int idade;

    public NinjaModel() {
    }

    public NinjaModel(String nome, String email, int idade) {
        this.nome = nome;
        this.email = email;
        this.idade = idade;
    }
}
